   public Pedido getPedido(){
        // conversion texto del jtextfield en un digito con decimales
        // conversion de numedicion en entero
        Integer tipos = Integer.parseInt(jTextField3.getText());
        Integer cantidad = Integer.parseInt(jTextField4.getText());
        String codMaterial = jCodMaterialTextField.getText();
        return new Pedido(jTextField1.getText(),codMaterial,jTextField2.getText(),tipos,cantidad);
    }

